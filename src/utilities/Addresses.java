package utilities;

public class Addresses {

	private String ipAddress;
	
	public Addresses() {
		this.ipAddress = "";
	}

	public Addresses(String ipAddress) {
		this.ipAddress = ipAddress;
	}

	public String getIpAddress() {
		return ipAddress;
	}

	public void setIpAddress(String ipAddress) {
		this.ipAddress = ipAddress;
	}
 
	

}
